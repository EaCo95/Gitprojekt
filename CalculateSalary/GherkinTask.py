def calculator(tal1, tal2, operator): 

    if operator == "+": 

        return tal1 + tal2 

    if operator == "-": 

        return tal1 - tal2         

    if operator == "/": 

        return tal1 / tal2                 

    if operator == "*": 

        return tal1 * tal2                         

    if operator == "%": 

        return tal1 % tal2                                 

    return None 

 